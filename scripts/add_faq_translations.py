def inject_faq_translations():
    with open(FILEPATH, "r", encoding="utf-8") as f:
        content = f.read()

    for scheme_id, translations in FAQ_TR.items():
        # Find the FAQ entry for this scheme_id within EXTRA_SECTIONS
        # Look for the pattern: "scheme_id": "xxx", ... "text_hi": (...), followed by },
        # We need to insert text_mr and text_ta before the closing },

        # Find the FAQ block start
        faq_marker = f'"scheme_id": "{scheme_id}"'
        # We only want the one in EXTRA_SECTIONS (after the schemes section)
        extra_start = content.index("EXTRA_SECTIONS")
        pos = content.find(faq_marker, extra_start)
        if pos == -1:
            print(f"✗ Could not find FAQ for '{scheme_id}'")
            continue

        # Check if text_mr already exists in this block
        next_scheme = content.find('"scheme_id":', pos + len(faq_marker))
        block_end = next_scheme if next_scheme != -1 else len(content)
        block = content[pos:block_end]
        if '"text_mr"' in block:
            print(f"⊘ FAQ '{scheme_id}' already has text_mr, skipping")
            continue

        # Find the end of text_hi for this FAQ block
        # text_hi ends with ),\n    },
        # We need to find the closing ), of text_hi after this scheme_id
        text_hi_pos = content.find('"text_hi":', pos)
        if text_hi_pos == -1 or text_hi_pos > block_end:
            print(f"✗ Could not find text_hi for FAQ '{scheme_id}'")
            continue

        # Find the closing ), of text_hi
        # The pattern is: "text_hi": (\n ... \n        ),
        # We need to find the closing ), which is followed by \n    },
        search_start = text_hi_pos
        depth = 0
        hi_end = -1
        i = content.index('(', search_start)
        depth = 1
        i += 1
        while i < len(content) and depth > 0:
            if content[i] == '(':
                depth += 1
            elif content[i] == ')':
                depth -= 1
            i += 1
        hi_end = i  # points right after the closing )

        # Now find the comma after )
        while hi_end < len(content) and content[hi_end] in ' \t':
            hi_end += 1
        if content[hi_end] == ',':
            hi_end += 1  # include the comma

        # Build the insertion text
        mr_text = translations["text_mr"]
        ta_text = translations["text_ta"]

        insertion = (
            '\n        "text_mr": (\n'
        )
        for line in mr_text.split('\n'):
            escaped = line.replace('\\', '\\\\').replace('"', '\\"')
            insertion += f'            "{escaped}\\n"\n'
        # Remove the last \n from the last line
        insertion = insertion.rstrip('\n')
        # Fix the last line - remove trailing \n" and replace with just "
        insertion = insertion[:-3] + '"\n'
        insertion += '        ),\n'

        insertion += '        "text_ta": (\n'
        for line in ta_text.split('\n'):
            escaped = line.replace('\\', '\\\\').replace('"', '\\"')
            insertion += f'            "{escaped}\\n"\n'
        insertion = insertion.rstrip('\n')
        insertion = insertion[:-3] + '"\n'
        insertion += '        ),\n'

        # Remove the trailing \n from hi_end position and insert
        # Actually, let's just insert after the text_hi closing ),
        # Find the newline after hi_end
        newline_pos = content.find('\n', hi_end)
        if newline_pos == -1:
            newline_pos = hi_end

        content = content[:newline_pos] + '\n' + insertion.rstrip('\n') + content[newline_pos:]
        print(f"✓ Injected mr/ta into FAQ '{scheme_id}'")

    with open(FILEPATH, "w", encoding="utf-8") as f:
        f.write(content)


if __name__ == "__main__":
    inject_faq_translations()
